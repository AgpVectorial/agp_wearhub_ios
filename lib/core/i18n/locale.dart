import 'package:flutter_riverpod/flutter_riverpod.dart';
import 'package:shared_preferences/shared_preferences.dart';

const _kLang = 'app.lang'; // language code

// Extensibil: adaugă noi coduri aici când vrei alte limbi
enum AppLang { ro, en, fr, de, it, es, pt, hu, pl, tr, ru, uk, bg }

extension AppLangCode on AppLang {
  String get code => switch (this) {
    AppLang.ro => 'ro',
    AppLang.en => 'en',
    AppLang.fr => 'fr',
    AppLang.de => 'de',
    AppLang.it => 'it',
    AppLang.es => 'es',
    AppLang.pt => 'pt',
    AppLang.hu => 'hu',
    AppLang.pl => 'pl',
    AppLang.tr => 'tr',
    AppLang.ru => 'ru',
    AppLang.uk => 'uk',
    AppLang.bg => 'bg',
  };

  static AppLang fromCode(String? code) {
    switch (code) {
      case 'fr':
        return AppLang.fr;
      case 'de':
        return AppLang.de;
      case 'it':
        return AppLang.it;
      case 'es':
        return AppLang.es;
      case 'pt':
        return AppLang.pt;
      case 'hu':
        return AppLang.hu;
      case 'pl':
        return AppLang.pl;
      case 'tr':
        return AppLang.tr;
      case 'ru':
        return AppLang.ru;
      case 'uk':
        return AppLang.uk;
      case 'bg':
        return AppLang.bg;
      case 'ro':
        return AppLang.ro;
      case 'en':
      default:
        return AppLang.en;
    }
  }
}

class LocaleController extends StateNotifier<AppLang> {
  LocaleController() : super(AppLang.en) {
    _load();
  }
  Future<void> _load() async {
    final p = await SharedPreferences.getInstance();
    state = AppLangCode.fromCode(p.getString(_kLang));
  }

  Future<void> set(AppLang lang) async {
    state = lang;
    final p = await SharedPreferences.getInstance();
    await p.setString(_kLang, lang.code);
  }
}

final localeProvider = StateNotifierProvider<LocaleController, AppLang>(
  (ref) => LocaleController(),
);

